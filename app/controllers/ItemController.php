	    if ($perPage > 0 && $page > 0) {
	        $items = Item::forPage($page, $perPage)->get();
	    } else {
		  $items = Item::all();
	    }
		if (isset($items)) {
		    return Response::json('{"items" : '.$items.'}');
		} else {
		    return Response::json('{"result" : "empty"}');
		}
	}

